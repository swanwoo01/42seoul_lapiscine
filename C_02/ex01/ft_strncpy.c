char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int	index;
	char			*dest_start;

	index = 0;
	dest_start = dest;
	while (src[index] && index < n)
	{
		dest[index] = src[index];
		index++;
	}
	while (index < n)
	{
		dest[index] = '\0';
		index++;
	}
	return (dest_start);
}
