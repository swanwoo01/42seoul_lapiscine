int	ft_sqrt(int nb)
{
	long long	x;

	x = 1;
	if (nb <= 0)
		return (0);
	while (x * x < (long long)nb)
		x++;
	if (x * x == (long long)nb)
		return ((int)x);
	return (0);
}
