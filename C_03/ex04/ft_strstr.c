char	*ft_strstr(char *str, char *to_find)
{
	int	flag;
	int	i;

	i = 0;
	while (*str)
	{
		i = 0;
		while (to_find[i] == str[i])
		{
			if (!to_find[i])
				return (str);
			i++;
		}
		str++;
	}
	return (NULL);
}
